package com.wenpu.jeelinks.modules.sys.web;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.apache.shiro.authz.annotation.RequiresPermissions;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.web.bind.annotation.RequestMapping;

import com.wenpu.jeelinks.common.persistence.Page;
import com.wenpu.jeelinks.common.web.BaseController;
import com.wenpu.jeelinks.modules.sys.entity.Log;
import com.wenpu.jeelinks.modules.sys.service.LogService;
import com.wenpu.jeelinks.modules.sys.utils.UserUtils;

/**
 * 日志Controller
 * @author ThinkGem
 * @version 2013-6-2
 */
@Controller
@RequestMapping(value = "${adminPath}/sys/log")
public class LogController extends BaseController {

	@Autowired
	private LogService logService;
	
	@RequiresPermissions("sys:log:view")
	@RequestMapping(value = {"list", ""})
	public String list(Log log, HttpServletRequest request, HttpServletResponse response, Model model) {
		if (!UserUtils.getUser().getCompany().getId().equals("1")) {
			log.setOfficeId(UserUtils.getUser().getCompany().getId());
		}
        Page<Log> page = logService.findPage(new Page<Log>(request, response), log); 
        model.addAttribute("page", page);
		return "modules/sys/logList";
	}

}
